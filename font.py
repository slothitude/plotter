CHAR_WIDTH = 7.0
CHAR_HEIGHT = 9.0


def text_to_strokes(text: str, x: float = 0, y: float = 0,
                    scale: float = 3.0, spacing: float = 1.5,
                    max_width: float | None = None,
                    line_spacing: float = 1.5,
                    align: str = "left") -> list[list[tuple[float, float]]]:
    """Convert text string to list of strokes (polylines).

    Args:
        text: String to render (uppercase + lowercase + symbols supported)
        x, y: Starting position (bottom-left of first line)
        scale: Character size multiplier
        spacing: Extra space between characters (in scaled units)
        max_width: If set, auto-wrap text to fit this width (in scaled units).
        line_spacing: Line height multiplier (default 1.5)
        align: Text alignment - "left", "center", "right"

    Returns:
        List of strokes, each stroke is a list of (x, y) tuples
    """
    all_strokes = []
    line_height = CHAR_HEIGHT * scale * line_spacing

    # Split on manual newlines first
    raw_lines = text.split('\n')

    # Word-wrap each line if max_width is set
    lines = []
    for raw_line in raw_lines:
        if not max_width or not raw_line:
            lines.append(raw_line)
            continue
        words = raw_line.split(' ')
        current_line = ''
        for word in words:
            if not word:
                continue
            test_line = (current_line + ' ' + word).strip() if current_line else word
            test_width = len(test_line) * (CHAR_WIDTH * scale + spacing) - spacing
            if test_width > max_width and current_line:
                lines.append(current_line)
                current_line = word
            else:
                current_line = test_line
        if current_line:
            lines.append(current_line)

    for i, line in enumerate(lines):
        ly = y + i * line_height

        # Calculate line width for alignment
        line_width = 0
        if line:
            line_width = len(line) * (CHAR_WIDTH * scale + spacing) - spacing

        # Calculate X offset based on alignment
        if align == "center" and max_width:
            cx = x + (max_width - line_width) / 2
        elif align == "right" and max_width:
            cx = x + max_width - line_width
        else:
            cx = x

        for ch in line:
            char_strokes = CHARS.get(ch, CHARS.get('?', []))
            for stroke in char_strokes:
                scaled = [(cx + px * scale, ly + (CHAR_HEIGHT - py) * scale) for px, py in stroke]
                all_strokes.append(scaled)
            cx += CHAR_WIDTH * scale + spacing

    return all_strokes


def measure_text(text: str, scale: float = 3.0, spacing: float = 1.5,
                 line_spacing: float = 1.5) -> tuple[float, float]:
    """Measure the width and height of text without rendering it.

    Returns:
        (width, height) tuple in scaled units
    """
    lines = text.split('\n')
    max_width = 0
    for line in lines:
        if line:
            w = len(line) * (CHAR_WIDTH * scale + spacing) - spacing
        else:
            w = 0
        max_width = max(max_width, w)

    height = len(lines) * CHAR_HEIGHT * scale * line_spacing
    return (max_width, height)
