# Average lowercase width for space calculation
CURSIVE_SPACE_WIDTH = 16


def _build_cursive_chains():
    """Precompute which strokes form the connected body vs extras for each letter.

    For each lowercase letter, find the 'exit stroke' (ends nearest to (width, 0)),
    then chain backwards through connected strokes. Connected strokes share endpoints
    (stroke N ends where stroke N+1 starts). Unconnected strokes (dots, crossbars)
    are extras.
    """
    chains = {}   # {char: [list of stroke indices forming the connected body]}
    extras = {}   # {char: [list of extra stroke indices]}

    for ch, strokes in CURSIVE_CHARS.items():
        if not strokes or not ch.islower():
            continue
        width = CURSIVE_WIDTHS.get(ch, CHAR_WIDTH)

        # Find exit stroke (last point nearest to (width, 0))
        exit_idx = 0
        best_dist = float('inf')
        for i, s in enumerate(strokes):
            lx, ly = s[-1]
            d = (lx - width) ** 2 + ly ** 2
            if d < best_dist:
                best_dist = d
                exit_idx = i

        # Chain backwards from exit stroke
        chain_indices = [exit_idx]
        for i in range(exit_idx - 1, -1, -1):
            prev_last = strokes[i][-1]
            chain_first = strokes[chain_indices[0]][0]
            # Connected if endpoints are close (within 2 units)
            if abs(prev_last[0] - chain_first[0]) <= 2 and abs(prev_last[1] - chain_first[1]) <= 2:
                chain_indices.insert(0, i)
            else:
                break

        chains[ch] = chain_indices
        extras[ch] = [i for i in range(len(strokes)) if i not in chain_indices]

    return chains, extras


_CURSIVE_CHAIN, _CURSIVE_EXTRAS = _build_cursive_chains()


def text_to_strokes(text: str, x: float = 0, y: float = 0,
                    scale: float = 3.0, spacing: float = 1.5,
                    max_width: float | None = None,
                    line_spacing: float = 1.5,
                    align: str = "left") -> list[list[tuple[float, float]]]:
    """Convert text string to list of strokes (polylines).

    Args:
        text: String to render (uppercase + lowercase + symbols supported)
        x, y: Starting position (bottom-left of first line)
        scale: Character size multiplier
        spacing: Extra space between characters (in scaled units)
        max_width: If set, auto-wrap text to fit this width (in scaled units).
        line_spacing: Line height multiplier (default 1.5)
        align: Text alignment - "left", "center", "right"

    Returns:
        List of strokes, each stroke is a list of (x, y) tuples
    """
    all_strokes = []
    line_height = CHAR_HEIGHT * scale * line_spacing

    # Split on manual newlines first
    raw_lines = text.split('\n')

    # Word-wrap each line if max_width is set
    lines = []
    for raw_line in raw_lines:
        if not max_width or not raw_line:
            lines.append(raw_line)
            continue
        words = raw_line.split(' ')
        current_line = ''
        for word in words:
            if not word:
                continue
            test_line = (current_line + ' ' + word).strip() if current_line else word
            test_width = len(test_line) * (CHAR_WIDTH * scale + spacing) - spacing
            if test_width > max_width and current_line:
                lines.append(current_line)
                current_line = word
            else:
                current_line = test_line
        if current_line:
            lines.append(current_line)

    for i, line in enumerate(lines):
        ly = y + i * line_height

        # Calculate line width for alignment
        line_width = 0
        if line:
            line_width = len(line) * (CHAR_WIDTH * scale + spacing) - spacing

        # Calculate X offset based on alignment
        if align == "center" and max_width:
            cx = x + (max_width - line_width) / 2
        elif align == "right" and max_width:
            cx = x + max_width - line_width
        else:
            cx = x

        for ch in line:
            char_strokes = CHARS.get(ch, CHARS.get('?', []))
            for stroke in char_strokes:
                scaled = [(cx + px * scale, ly + (CHAR_HEIGHT - py) * scale) for px, py in stroke]
                all_strokes.append(scaled)
            cx += CHAR_WIDTH * scale + spacing

    return all_strokes


def _cursive_line_width(line: str, scale: float, spacing: float) -> float:
    """Calculate the rendered width of a line using variable-width cursive glyphs."""
    if not line:
        return 0
    w = 0
    for ch in line:
        cw = CURSIVE_WIDTHS.get(ch, CHAR_WIDTH)
        w += cw * scale + spacing
    return w - spacing  # no trailing space


def text_to_cursive(text: str, x: float = 0, y: float = 0,
                    scale: float = 3.0, spacing: float = 1.5,
                    max_width: float | None = None,
                    line_spacing: float = 1.5,
                    align: str = "left") -> list[list[tuple[float, float]]]:
    """Convert text to cursive strokes using Hershey Script Simplex font.

    Lowercase letters within a word are chained into continuous strokes
    (pen stays down between letters via connecting lines), minimizing pen lifts.
    Uppercase, digits, and punctuation are rendered as separate strokes
    using the cursive glyph shapes.
    """
    all_strokes: list[list[tuple[float, float]]] = []
    line_height = CHAR_HEIGHT * scale * line_spacing

    # Word-wrap with variable widths
    raw_lines = text.split('\n')
    lines: list[str] = []
    for raw_line in raw_lines:
        if not max_width or not raw_line:
            lines.append(raw_line)
            continue
        words = raw_line.split(' ')
        current_line = ''
        current_w = 0.0
        for word in words:
            if not word:
                continue
            word_w = _cursive_line_width(word, scale, spacing)
            space_w = CURSIVE_SPACE_WIDTH * scale + spacing if current_line else 0
            test_w = current_w + space_w + word_w
            if test_w > max_width and current_line:
                lines.append(current_line)
                current_line = word
                current_w = word_w
            else:
                current_line = (current_line + ' ' + word).strip()
                current_w = test_w
        if current_line:
            lines.append(current_line)

    for i, line in enumerate(lines):
        ly = y + i * line_height

        # Calculate line width for alignment
        line_width = _cursive_line_width(line, scale, spacing)
        if align == "center" and max_width:
            cx = x + (max_width - line_width) / 2
        elif align == "right" and max_width:
            cx = x + max_width - line_width
        else:
            cx = x

        current_stroke: list[tuple[float, float]] = []
        extra_strokes: list[list[tuple[float, float]]] = []

        for ch in line:
            if ch == ' ':
                if len(current_stroke) >= 2:
                    all_strokes.append(current_stroke)
                all_strokes.extend(extra_strokes)
                current_stroke = []
                extra_strokes = []
                cx += CURSIVE_SPACE_WIDTH * scale + spacing
                continue

            is_cursive_lower = ch.islower() and ch in CURSIVE_CHARS
            char_strokes = CURSIVE_CHARS.get(ch, CHARS.get(ch, CHARS.get('?', [])))
            cw = CURSIVE_WIDTHS.get(ch, CHAR_WIDTH)

            if is_cursive_lower:
                # Get chain (connected body) and extra strokes
                chain_indices = _CURSIVE_CHAIN.get(ch, [0])
                extra_indices = _CURSIVE_EXTRAS.get(ch, [])

                # Scale all strokes for this character
                scaled = []
                for stroke in char_strokes:
                    scaled.append([(cx + px * scale, ly + (CHAR_HEIGHT - py) * scale)
                                   for px, py in stroke])

                # Merge chain strokes into the connected word stroke
                for ci, idx in enumerate(chain_indices):
                    s = scaled[idx]
                    if ci == 0:
                        if current_stroke:
                            # Add connecting line from previous letter
                            current_stroke.append(s[0])
                        current_stroke.extend(s)
                    else:
                        # Strokes within the chain connect naturally
                        current_stroke.extend(s)

                # Extra strokes (dots, crossbars) are separate
                for idx in extra_indices:
                    s = scaled[idx]
                    if len(s) >= 2:
                        extra_strokes.append(s)
            else:
                # Uppercase / digits / punctuation: flush word stroke, render separately
                if len(current_stroke) >= 2:
                    all_strokes.append(current_stroke)
                all_strokes.extend(extra_strokes)
                current_stroke = []
                extra_strokes = []
                for stroke in char_strokes:
                    sc = [(cx + px * scale, ly + (CHAR_HEIGHT - py) * scale)
                          for px, py in stroke]
                    if len(sc) >= 2:
                        all_strokes.append(sc)

            cx += cw * scale + spacing

        # End of line — flush remaining strokes
        if len(current_stroke) >= 2:
            all_strokes.append(current_stroke)
        all_strokes.extend(extra_strokes)

    return all_strokes


def measure_text(text: str, scale: float = 3.0, spacing: float = 1.5,
                 line_spacing: float = 1.5, font_style: str = "hershey") -> tuple[float, float]:
    """Measure the width and height of rendered text.

    Uses actual character widths from the font data for accuracy.
    Cursive uses CURSIVE_WIDTH, Hershey uses CHAR_WIDTH.

    Returns:
        (width, height) tuple in scaled units
    """
    lines = text.split('\n')
    max_width = 0
    char_w = CHAR_WIDTH
    if font_style == "cursive":
        # Use average cursive width as estimate (individual chars vary)
        all_widths = list(CURSIVE_WIDTHS.values())
        char_w = sum(all_widths) / len(all_widths) if all_widths else CHAR_WIDTH
    for line in lines:
        if line:
            w = len(line) * (char_w * scale + spacing) - spacing
        else:
            w = 0
        max_width = max(max_width, w)

    height = len(lines) * CHAR_HEIGHT * scale * line_spacing
    return (max_width, height)
